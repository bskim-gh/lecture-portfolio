"""
Ch07 - Python 정규표현식
========================

주요 내용:
- 정규표현식(Regular Expression) 개념
- re 모듈 import
- findall() 함수: 패턴과 일치하는 모든 문자열 검색
- match() 함수: 문자열의 시작 부분부터 패턴 매칭
- 숫자 패턴: [0-9], {n}, {n,}
- 문자 패턴: [a-z], [A-Z], [가-힣]
- 특수 문자 패턴: ^(시작), $(끝), \\w(단어 문자)
- 주민등록번호 유효성 검사
- 문자열 찾기, 치환, 선택
- 전처리 함수
"""

# ====================================
# 1. 정규표현식 기본
# ====================================

import re
from re import findall, match

str1 = "1234 abc홍길동 ABC_555_6 이사도시"

# 숫자 검색
print(findall("1234", str1))
print(findall("[0-9]", str1))
print(findall("[0-9]{3}", str1))
print(findall("[0-9]{3,}", str1))

# 문자열 검색
print(findall("[가-힣]{3,}", str1))
print(findall("[a-z|A-Z]{3,}", str1))


# ====================================
# 2. 위치 패턴 (^, $)
# ====================================

str2 = 'test1abcABC 123mbc 45test'

print(findall('^test', str2))  # 문자열의 시작
print(findall("st$", str2))    # 문자열의 끝


# ====================================
# 3. 단어 문자 검색 (\\w)
# ====================================

str3 = 'test^홍길동 abc 대한*민국 123$tbc'

result = findall("\\w{3,}", str3)
print(result)


# ====================================
# 4. 주민등록번호 유효성 검사
# ====================================

# 올바른 주민등록번호
jumin = '123456-3235678'
result = match('[0-9]{6}-[1-4][0-9]{6}', jumin)

if result:
    print('결과 매칭')
else:
    print('결과 매칭 안됨')

# 잘못된 주민등록번호 (성별자리가 5)
jumin2 = '123456-5235678'
result2 = match('[0-9]{6}-[1-4][0-9]{6}', jumin2)

if result2:
    print('결과 매칭')
else:
    print('결과 매칭 안됨')

